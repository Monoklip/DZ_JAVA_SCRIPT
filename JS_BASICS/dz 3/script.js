// Створіть програму, яка обчислює факторіал натурального числа n, яке користувач введе з клавіатури.


function exs3_1() {

  let f = prompt('Введіть число');

  let j = 1;

  for(let i = 2; i <= f; i++) {
    j = j*i;
  }

  alert(j);

}



