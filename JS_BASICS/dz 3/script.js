// Завдання 8
// Написати програму що обчислює піднесення до степеня. В 1й промпт вводиться число,а в 2й промпт степінь. Умови: Степінь може бути додатня, від'ємна або 0. Обрахувати всі можливі варіанти циклом for. Задачу бажано виконати використовуючи 1 цикл! 
// a^b = a*a b раз;
// a^-b= 1/a^b;
// a^0 = 1;



function exs3_8() {

  let a = prompt("Число");
  let b = prompt("Степінь")

  let n = a;

  for (let i = 1; i < b; i++) {
    n *= a;
  }


  alert(n);
}

